class Solution:
    # @param {integer} n
    # @return {integer[]}
    def grayCode(self, n):
        if n == 1:
            return [0, 1]
        ret = self.grayCode(n-1)
        for i, num in enumerate(reversed(ret)):
            ret.append(num + pow(2, n-1))
        return ret



